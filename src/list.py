friends = ["micky", "vicky", "tikki", "money", "sunny"]
print(friends)
# get specific elements from the list using the index
print(friends[0])
print(friends[1])
print(friends[2])

# get the range of elements from the list
# it give everything after the initial index mentioned
print(friends[1:])

# it gives the range but not including the last index mentioned
print(friends[1:4])
